-- NUESTRA LOS 100 PRIMEROS NUMEROS PRIMOS
DECLARE
PRIMO BOOLEAN;
NPRIMOS INTEGER:=1;
N INTEGER:=2;
I INTEGER;

BEGIN

DBMS_OUTPUT.PUT_LINE(1);
WHILE NPRIMOS<100 LOOP
	PRIMO:=TRUE;
	I:=2;
	WHILE (i<= N/2 AND PRIMO) LOOP
		IF MOD(N,I)=0 THEN
			PRIMO:=FALSE;
		END IF;
		I:=I+1;
	END  LOOP;
	IF PRIMO THEN
		DBMS_OUTPUT.PUT_LINE(N);
		NPRIMOS:=NPRIMOS+1;
	END IF;
	N:=N+1;
END LOOP;
END;
/
